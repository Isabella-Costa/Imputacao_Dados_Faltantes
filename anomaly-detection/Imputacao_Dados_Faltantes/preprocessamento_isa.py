# Função auxiliar para calcular o RMS médio de todas as janelas para cada sensor
def calcular_rms_medio(tensor):
    # tensor.shape = (janelas, 2048, 6)
    # Eleva ao quadrado, tira a média no eixo do tempo (2048), tira a raiz, depois a média das janelas
    rms_por_janela = np.sqrt(np.mean(tensor**2, axis=1))
    rms_medio = np.mean(rms_por_janela, axis=0)
    return rms_medio

