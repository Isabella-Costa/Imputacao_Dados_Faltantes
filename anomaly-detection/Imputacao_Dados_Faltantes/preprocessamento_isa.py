class ProcessadorAcelerometro:
    def __init__(self, caminho_arquivo, fs=50000):
        self.caminho = caminho_arquivo
        self.fs = fs
        self.df = self.extrair_dados()

    def extrair_dados(self):
        try:
            mat = scipy.io.loadmat(self.caminho)
            dados_struct = mat['data'][0, 0]['Analog50k']

            campos = [
                'TDMSAnalog50kAcelerometer0', 'TDMSAnalog50kAcelerometer1',
                'TDMSAnalog50kAcelerometer2', 'TDMSAnalog50kAcelerometer3',
                'TDMSAnalog50kAcelerometer4', 'TDMSAnalog50kAcelerometer5'
            ]
            nomes_colunas = ['A0_Motor', 'A1_Pinion_H', 'A2_Pinion_V', 'A3_Gear_V', 'A4_Bearing_G', 'A5_Bearing_M']

            lista_sinais = []
            for campo in campos:
                sinal_bruto = dados_struct[campo][0, 0].flatten()
                lista_sinais.append(sinal_bruto)

            matriz_dados = np.array(lista_sinais, dtype=float).T
            df = pd.DataFrame(matriz_dados, columns=nomes_colunas)
            df['Tempo_s'] = np.arange(len(df)) / self.fs
            return df
        except Exception as e:
            print(f"Erro detalhado em {self.caminho}: {str(e)}")
            return pd.DataFrame()

