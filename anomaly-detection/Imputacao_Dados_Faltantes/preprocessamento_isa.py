class Preprocessamento:
    def __init__(self, tamanho_janela=2048, overlap=0.5, tipo_scaler='robust'):
        self.tamanho_janela = tamanho_janela
        self.overlap = overlap
        self.tipo_scaler = tipo_scaler.lower()

        #  de normalização
        if self.tipo_scaler == 'standard':
            self.scaler = StandardScaler()
        elif self.tipo_scaler == 'robust':
            self.scaler = RobustScaler()
        else:
            raise ValueError("O parâmetro tipo_scaler deve ser 'standard' ou 'robust'.")

    def separar_tempo_sensores(self, df):
        tempo = df.iloc[:, -1].to_numpy()
        dados = df.iloc[:, :-1].to_numpy()
        return dados, tempo

    def divisao_temporal(self, dados, proporcao_treino=0.8):
        indice_corte = int(len(dados) * proporcao_treino)
        treino = dados[:indice_corte]
        teste = dados[indice_corte:]
        return treino, teste

    def normalizar_dados(self, treino, teste):
        # Aprende os parâmetros APENAS do treino
        self.scaler.fit(treino)

        # Aplica a transformação em ambos
        treino_norm = self.scaler.transform(treino)
        teste_norm = self.scaler.transform(teste)

        return treino_norm, teste_norm

    def criar_janelas(self, dados):
        passo = int(self.tamanho_janela * (1 - self.overlap))
        janelas = []

        # percorre capturando os blocos e transformando em tensores
        for inicio in range(0, len(dados) - self.tamanho_janela + 1, passo):
            fim = inicio + self.tamanho_janela
            janelas.append(dados[inicio:fim])

        return np.array(janelas)

    def executar_pipeline_arquivo_unico(self, df):
        dados, _ = self.separar_tempo_sensores(df)
        treino_raw, teste_raw = self.divisao_temporal(dados, proporcao_treino=0.8)
        treino_norm, teste_norm = self.normalizar_dados(treino_raw, teste_raw)
        X_treino = self.criar_janelas(treino_norm)
        X_teste = self.criar_janelas(teste_norm)
        return X_treino, X_teste

