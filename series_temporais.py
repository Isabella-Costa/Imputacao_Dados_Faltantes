import numpy as np
import matplotlib.pyplot as plt

# --- 1. Parâmetros da Simulação ---
n_amostras = 20         # O número de ondas que queremos gerar
tam_vetor = 1000        # Número de pontos em cada onda (resolução)
amplitude = 1.5         # Amplitude (altura) de todas as ondas
frequencia = 2          # Frequência em Hertz (quantos ciclos por segundo)
tempo_total_s = 5       # Duração da simulação em segundos

# --- 2. Criação do Eixo do Tempo ---
# Cria um vetor de tempo que vai de 0 a tempo_total_s, com tam_vetor pontos.
tempo = np.linspace(0, tempo_total_s, tam_vetor)

# --- 3. Preparação do Gráfico ---
# Cria a figura e os eixos para o plot. O figsize controla o tamanho da janela.
plt.figure(figsize=(14, 8))

# --- 4. Loop Principal para Gerar e Plotar as Ondas ---
print(f"Gerando e plotando {n_amostras} ondas senoidais...")

for i in range(n_amostras):
    # O PONTO-CHAVE: Gera uma nova fase aleatória para CADA onda.
    # O valor é sorteado entre 0 e 2*pi (um ciclo completo).
    fase_aleatoria = np.random.uniform(0, 2 * np.pi)

    # Calcula a senoide usando a fórmula completa: A * sin(2*pi*f*t + phi)
    senoide = amplitude * np.sin(2 * np.pi * frequencia * tempo + fase_aleatoria)

    # Plota a onda recém-criada no gráfico.
    # O alpha=0.7 deixa as linhas um pouco transparentes para melhor visualização.
    plt.plot(tempo, senoide, alpha=0.7)

# --- 5. Finalização do Gráfico ---
# Adiciona títulos e rótulos para deixar o gráfico informativo.
plt.title(f'{n_amostras} Ondas Senoidais com Mesma Amplitude e Frequência', fontsize=16)
plt.xlabel('Tempo (s)', fontsize=12)
plt.ylabel('Amplitude', fontsize=12)
plt.grid(True) # Adiciona uma grade de fundo para facilitar a leitura.
plt.ylim(-amplitude * 1.2, amplitude * 1.2) # Define um limite vertical para o eixo Y.

# Exibe o gráfico final na tela.
plt.show()

print("Gráfico exibido com sucesso!")

