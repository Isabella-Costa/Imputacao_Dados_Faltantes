import numpy as np
import os #Operating System - manipular diretório, arquivos e caminhos

class GeradorSeriesTemporais:
    """
    Classe para gerar e manipular séries temporais multivariadas.
    """
    def __init__(self, n_amostras=20, tam_vetor=1000): #Construtor - inicializar o estado do objeto
        """
        Inicializa o gerador.

        Args:
            n_amostras (int): Número de amostras de séries temporais (padrão: 20).
            tam_vetor (int): Tamanho do vetor de tempo (padrão: 1000).
        """
        self.n_amostras = n_amostras
        self.tam_vetor = tam_vetor
        self.tempo = np.linspace(0, 10, tam_vetor) # Vetor de tempo de 0 a 10 segundos

    def _gerar_senoides(self, amplitude, frequencia):
      #nomeclatura iniciada em _ (_gerar_senoides) indica que o método é para uso interno da classe
        """
        Gera um conjunto de senoides com a mesma amplitude e frequência.

        Args:
            amplitude (float): Amplitude da senoide.
            frequencia (float): Frequência angular (rad/s) da senoide.

        Returns:
            np.ndarray: Array 2D (n_amostras, tam_vetor) com as senoides geradas.
        """
        # Cria uma matriz 2D para armazenar todas as amostras
        senoides = np.zeros((self.n_amostras, self.tam_vetor))
        for i in range(self.n_amostras):
            # Adiciona uma pequena fase aleatória para diferenciar os sinais
            fase_aleatoria = np.random.uniform(0, 2 * np.pi) #deslocamento horizontal no gráfico, completa o ciclo de 360°
            #cada onda tem um ponto de partida (fase) diferente
            senoides[i, :] = amplitude * np.sin(frequencia * self.tempo + fase_aleatoria) # função seno: A * sin(ωt + φ)
        return senoides

    def criar_cenario(self, config_sinais, nome_arquivo):
        """
        Cria um cenário completo com 3 tipos de senoides e salva em .npy.

        Args:
            config_sinais (list of tuples): Lista contendo tuplas (amplitude, frequencia)
                                            para cada um dos 3 sinais.
            nome_arquivo (str): Nome do arquivo .npy para salvar os dados.
        """
        print(f"Gerando cenário para '{nome_arquivo}'...")

        # Gera os 3 conjuntos de 20 senoides
        senoides1 = self._gerar_senoides(config_sinais[0][0], config_sinais[0][1])
        senoides2 = self._gerar_senoides(config_sinais[1][0], config_sinais[1][1])
        senoides3 = self._gerar_senoides(config_sinais[2][0], config_sinais[2][1])

        # Empilha os 3 conjuntos para formar a matriz final (20, 3, 1000)
        # np.stack cria uma nova dimensão
        matriz_final = np.stack([senoides1, senoides2, senoides3], axis=1)

        '''Função axis - eixo determina o índice (a posição) onde a nova dimensão
        será inserida no .shape da matriz final.
        axis = 0 --> (3,20,1000)
        axis = 1 --> (20,3,1000)
        axis = 2 --> (20,1000,3) '''

        print(f"Dimensão da matriz resultante: {matriz_final.shape}")

        # Salva a matriz em um arquivo .npy
        np.save(nome_arquivo, matriz_final)
        print(f"Matriz salva em '{nome_arquivo}.npy'\n")

        return matriz_final

   
    ''' 
    Decorador - transforma em um método estático. Pertence à classe, mas não depende
        de um objeto específico (ele não recebe o self) e não precisa acessar atributos
        da instância.
        '''
    @staticmethod
    def remover_dados_mcar(dados, percentual, nome_arquivo_saida): #MCAR: Missing Completely at Random

        """
        Remove um percentual de dados de uma série temporal (mecanismo MCAR).

        Args:
            dados (np.ndarray): Matriz de dados original.
            percentual (float): Percentual de dados a serem removidos (ex: 0.10 para 10%).
            nome_arquivo_saida (str): Nome do arquivo para salvar a matriz com dados faltantes.
        """

        print(f"Removendo {percentual*100:.0f}% dos dados de '{nome_arquivo_saida}'...")

        # Cria uma cópia para não alterar o array original
        dados_com_faltantes = dados.copy()

        # Calcula o número total de elementos e quantos remover
        n_total_elementos = dados.size
        n_a_remover = int(n_total_elementos * percentual)

        # Gera uma lista de todos os índices possíveis ( achatando o array)
        indices = np.arange(n_total_elementos) #cria uma sequência de número de 0 até o total de elementos

        # Escolhe aleatoriamente os índices que serão removidos, são9 índices planos (0 a 5999)
        indices_para_remover = np.random.choice(indices, size=n_a_remover, replace=False) #replace = false --> sem repetições

        # Converte os índices "planos" de volta para coordenadas 3D
        coords_para_remover = np.unravel_index(indices_para_remover, dados.shape)
        # numpy.unravel_index(indices, shape, order='C')

        # Substitui os valores nos índices escolhidos por NaN (Not a Number)
        dados_com_faltantes[coords_para_remover] = np.nan

        # Salva o novo array
        np.save(nome_arquivo_saida, dados_com_faltantes)
        print(f"Dados com valores faltantes salvos em '{nome_arquivo_saida}.npy'\n")
        return dados_com_faltantes

